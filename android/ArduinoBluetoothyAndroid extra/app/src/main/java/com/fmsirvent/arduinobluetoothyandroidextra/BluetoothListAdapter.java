package com.fmsirvent.arduinobluetoothyandroidextra;

import android.bluetooth.BluetoothDevice;
import android.content.Context;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.BaseAdapter;
import android.widget.TextView;

import java.util.ArrayList;

public class BluetoothListAdapter extends BaseAdapter {
    private ArrayList<BluetoothDevice> devices;

    public BluetoothListAdapter(ArrayList<BluetoothDevice> bondedDevices) {
        devices = bondedDevices;
    }

    @Override
    public int getCount() {
        return devices.size();
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        LayoutInflater inflater = (LayoutInflater) parent.getContext()
                .getSystemService(Context.LAYOUT_INFLATER_SERVICE);
        View rowView = inflater.inflate(android.R.layout.simple_list_item_1, parent, false);
        TextView textView1 = (TextView) rowView.findViewById(android.R.id.text1);
        textView1.setText(devices.get(position).getName());
        return rowView;
    }

    @Override
    public Object getItem(int position) {
        return devices.get(position);
    }

    @Override
    public long getItemId(int position) {
        return position;
    }
}

